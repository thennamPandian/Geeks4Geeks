public class DoublyLLWithObject {

	private Object data;

	private DoublyLLWithObject prev;

	private DoublyLLWithObject next;

	public Object getData() {
		return data;
	}

	public void setData(Object data) {
		this.data = data;
	}

	public DoublyLLWithObject getPrev() {
		return prev;
	}

	public void setPrev(DoublyLLWithObject prev) {
		this.prev = prev;
	}

	public DoublyLLWithObject getNext() {
		return next;
	}

	public void setNext(DoublyLLWithObject next) {
		this.next = next;
	}

}
